DATA_SEG SEGMENT 
	STRING	DB	'??????$'
	LEN	DB	0
DATA_SEG ENDS

CODE_SEG SEGMENT
	ASSUME DS:DATA_SEG, CS:CODE_SEG
	LEA	SI, STRING 
	MOV	BX, $0
	JMP	L1
L0:	INC	BX
	INC	SI
L1:	MOV	AL, [SI]
	CMP	AL, '$'
	JNE	L0
	MOV	LEN, BX
	LEA	SI, STRING
	ADD	SI, LEN-1
	LEA	DI, STRING+2
	ADD	DI, LEN-1
	MOV	CX, BX
L2:	MOV	AL, [SI]
	MOV	[DI], AL
	DEC	SI
	DEC	DI
	LOOP	L2
	MOV	[SI], BX
EXIT:	MOV	AX, 4C00H
	INT	21H
CODE_SEG ENDS